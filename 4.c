#include <stdio.h>

int main(void)
{
	char lowercase;

	for (lowercase = 'a'; lowercase <= 'z'; lowercase++)
		if (lowercase != 'e' && lowercase != 'q')
			putchar(lowercase);

			char uppercase;
	for (uppercase = 'A'; uppercase <= 'Z'; uppercase++)
		if (uppercase != 'E' && uppercase != 'Q')

		putchar(uppercase);

			putchar('\n');

			return (0);

}
